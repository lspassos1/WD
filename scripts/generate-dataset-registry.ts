import { mkdirSync, writeFileSync } from 'node:fs';
import { DATASETS, type DatasetContract } from '../registry/datasets';

function fail(msg: string): never {
  throw new Error(`[dataset-registry] ${msg}`);
}

function hasCurrentYearPlaceholder(key: string): boolean {
  return key.includes('{currentYear}');
}

function hasMatchingVersionTag(key: string, versionTag: `v${number}`): boolean {
  const matcher = new RegExp(`(?:^|[:\\-])${versionTag}(?:$|[:])`);
  return matcher.test(key);
}

function validateDataset(dataset: DatasetContract): void {
  if (!dataset.owner?.github) {
    fail(`Missing owner.github for ${dataset.id}`);
  }

  if (dataset.redis.versionTag && !hasMatchingVersionTag(dataset.redis.key, dataset.redis.versionTag)) {
    fail(`Dataset ${dataset.id} has key/version mismatch`);
  }

  if (!dataset.bootstrap) {
    return;
  }

  if (dataset.bootstrap.redisReadMode !== 'unprefixed') {
    fail(`Dataset ${dataset.id} has invalid bootstrap.redisReadMode`);
  }

  if (!dataset.health) {
    fail(`Dataset ${dataset.id} is bootstrap-enabled but missing health contract`);
  }

  if (hasCurrentYearPlaceholder(dataset.redis.key)) {
    fail(`Dataset ${dataset.id} uses a runtime key template and cannot be bootstrap-enabled`);
  }

  if (dataset.cacheFill?.enabled) {
    if (!dataset.cacheFill.leaseMs || !dataset.cacheFill.waitMs) {
      fail(`Dataset ${dataset.id} enables cacheFill but leaseMs/waitMs are incomplete`);
    }
    if (dataset.cacheFill.waitMs >= dataset.cacheFill.leaseMs) {
      fail(`Dataset ${dataset.id} has invalid cache-fill timing: waitMs must be < leaseMs`);
    }
  }
}

function validateGlobal(datasets: DatasetContract[]): void {
  const ids = new Set<string>();
  const redisKeys = new Set<string>();
  const bootstrapAliases = new Set<string>();

  for (const dataset of datasets) {
    validateDataset(dataset);

    if (ids.has(dataset.id)) {
      fail(`Duplicate dataset id: ${dataset.id}`);
    }
    ids.add(dataset.id);

    if (redisKeys.has(dataset.redis.key)) {
      fail(`Duplicate redis key: ${dataset.redis.key}`);
    }
    redisKeys.add(dataset.redis.key);

    if (!dataset.bootstrap) {
      continue;
    }

    if (bootstrapAliases.has(dataset.bootstrap.alias)) {
      fail(`Duplicate bootstrap alias: ${dataset.bootstrap.alias}`);
    }
    bootstrapAliases.add(dataset.bootstrap.alias);
  }
}

function stable<T>(items: T[], key: (item: T) => string): T[] {
  return [...items].sort((a, b) => key(a).localeCompare(key(b)));
}

function formatStringMap(entries: Array<[string, string]>): string {
  if (entries.length === 0) {
    return '{}';
  }

  return `{\n${entries
    .map(([name, value]) => `  ${JSON.stringify(name)}: ${JSON.stringify(value)},`)
    .join('\n')}\n}`;
}

function formatTierMap(entries: Array<[string, string]>): string {
  if (entries.length === 0) {
    return '{}';
  }

  return `{\n${entries
    .map(([name, value]) => `  ${JSON.stringify(name)}: ${JSON.stringify(value)},`)
    .join('\n')}\n}`;
}

function emit(): void {
  validateGlobal(DATASETS);

  const bootstrapDatasets = stable(
    DATASETS.filter((dataset) => dataset.bootstrap),
    (dataset) => dataset.bootstrap!.alias,
  );
  const bootstrapCacheKeys = bootstrapDatasets.map((dataset) => [dataset.bootstrap!.alias, dataset.redis.key] as const);
  const bootstrapTiers = bootstrapDatasets.map((dataset) => [dataset.bootstrap!.alias, dataset.bootstrap!.tier] as const);

  mkdirSync('server/_shared/_generated', { recursive: true });
  mkdirSync('api/_generated', { recursive: true });

  writeFileSync(
    'server/_shared/_generated/bootstrap-registry.ts',
    [
      '/* AUTO-GENERATED by scripts/generate-dataset-registry.ts. DO NOT EDIT. */',
      `export const BOOTSTRAP_CACHE_KEYS: Record<string, string> = ${formatStringMap(bootstrapCacheKeys)};`,
      `export const BOOTSTRAP_TIERS: Record<string, 'slow' | 'fast'> = ${formatTierMap(bootstrapTiers)};`,
      '',
    ].join('\n'),
  );

  writeFileSync(
    'api/_generated/dataset-registry.js',
    [
      '/* AUTO-GENERATED by scripts/generate-dataset-registry.ts. DO NOT EDIT. */',
      `export const BOOTSTRAP_CACHE_KEYS = ${formatStringMap(bootstrapCacheKeys)};`,
      `export const BOOTSTRAP_TIERS = ${formatTierMap(bootstrapTiers)};`,
      '',
    ].join('\n'),
  );
}

emit();
